const PIE = 3.14;

function areaOfCircle(radius) {
    console.log("area of circle with radius " + radius + " is: " + PIE*(radius*radius))
}

function areaOfRectangle(length, width) {
    console.log("area of rectangle with length and width " + length + " " + width + " is " + (length * width))
}

areaOfCircle(5)
areaOfRectangle(20, 22)

